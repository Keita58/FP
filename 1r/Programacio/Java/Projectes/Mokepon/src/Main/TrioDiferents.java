package Main;

public class TrioDiferents<T, R, S> {

	private T primer;
	private R segon;
	private S tercer;
	
	public T getPrimer() {
		return primer;
	}
	public void setPrimer(T primer) {
		this.primer = primer;
	}
	public R getSegon() {
		return segon;
	}
	public void setSegon(R segon) {
		this.segon = segon;
	}
	public S getTercer() {
		return tercer;
	}
	public void setTercer(S tercer) {
		this.tercer = tercer;
	}
}
